//! §D.8 32-band interpolation FIR coefficient tables for the DTS
//! Core 32-band synthesis QMF.
//!
//! Transcribed verbatim from ETSI TS 102 114 V1.3.1 (2011-08)
//! Annex D §D.8 "32-Band Interpolation and LFE Interpolation FIR"
//! (staged at `docs/audio/dts/etsi-ts-102114-dts-coherent-acoustics.pdf`,
//! PDF p.238-246), columns "Perfect Reconstruction" and "Non-Perfect
//! Reconstruction" (indices 0..=511; the spec table prints decimal
//! commas, rendered here as decimal points). These are the two
//! 512-tap `prCoeff` sets the §C.2.5 `QMFInterpolation()` FIR step
//! convolves against the `raX[]` shift register
//! (`docs/audio/dts/dts-core-extracts.md` §2.4):
//!
//! - [`RA_COEFF_LOSSLESS`] is the spec pseudocode's `raCoeffLossLess`
//!   identifier — the **perfect reconstruction** set, selected by the
//!   `else` branch of `if (FILTS==0) ...` (i.e. `FILTS != 0`).
//! - [`RA_COEFF_LOSSY`] is the spec pseudocode's `raCoeffLossy`
//!   identifier — the **non-perfect reconstruction** set, selected by
//!   the `FILTS == 0` branch.
//!
//! [`crate::FilterBankSelection::coefficients`] resolves the typed
//! round-263 selector to the matching table.
//!
//! The same §D.8 table also carries two LFE interpolation FIR columns
//! ("64 x Interpolation" / "128 x Interpolation"); those drive the
//! LFE decimation/interpolation path, not the 32-band synthesis QMF,
//! and live in a separate module ([`crate::lfe_fir_coeff`], selected
//! by [`crate::LfeInterpolationSelection`]).
//!
//! Both tables are exactly antisymmetric in their printed digits
//! (`coeff[i] == -coeff[511 - i]` for every `i`); the test suite
//! verifies that whole-table property plus verbatim anchor rows at
//! every page boundary of the staged PDF (p.238/239/240/241/242/
//! 243/244/245/246).

