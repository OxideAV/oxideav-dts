    #[test]
    fn page_anchor_rows_match_the_staged_pdf_verbatim() {
        for &(i, pr, npr) in PAGE_ANCHORS {
            assert_eq!(
                RA_COEFF_LOSSLESS[i].to_bits(),
                pr.to_bits(),
                "RA_COEFF_LOSSLESS[{i}] != PDF Perfect-Reconstruction row {i}"
            );
            assert_eq!(
                RA_COEFF_LOSSY[i].to_bits(),
                npr.to_bits(),
                "RA_COEFF_LOSSY[{i}] != PDF Non-Perfect-Reconstruction row {i}"
            );
        }
    }

    #[test]
    fn both_tables_are_exactly_antisymmetric() {
        // The printed §D.8 digits satisfy coeff[i] == -coeff[511-i]
        // for every row of both 32-band columns (verified across the
        // staged pages); a transcription error in either half breaks
        // the pairing against the verbatim other half.
        for i in 0..FIR_COEFF_LEN {
            let j = FIR_COEFF_LEN - 1 - i;
            assert_eq!(
                RA_COEFF_LOSSLESS[i].to_bits(),
                (-RA_COEFF_LOSSLESS[j]).to_bits(),
                "RA_COEFF_LOSSLESS[{i}] != -RA_COEFF_LOSSLESS[{j}]"
            );
            assert_eq!(
                RA_COEFF_LOSSY[i].to_bits(),
                (-RA_COEFF_LOSSY[j]).to_bits(),
                "RA_COEFF_LOSSY[{i}] != -RA_COEFF_LOSSY[{j}]"
            );
        }
    }

    #[test]
    fn tables_are_finite_and_bounded_by_the_printed_peak() {
        // Every printed magnitude is at most the centre rows
        // (1.892151600e-02 perfect / 1.788828894e-02 non-perfect).
        for i in 0..FIR_COEFF_LEN {
            assert!(RA_COEFF_LOSSLESS[i].is_finite());
            assert!(RA_COEFF_LOSSY[i].is_finite());
            assert!(
                RA_COEFF_LOSSLESS[i].abs() <= 1.892151600e-02,
                "RA_COEFF_LOSSLESS[{i}] exceeds the printed peak"
            );
            assert!(
                RA_COEFF_LOSSY[i].abs() <= 1.788828894e-02,
                "RA_COEFF_LOSSY[{i}] exceeds the printed peak"
            );
        }
    }

    #[test]
    fn peak_magnitude_sits_at_the_table_centre() {
        // The §D.8 magnitudes ramp up to the centre rows 255/256 in
        // both columns; the maxima land exactly there.
        let max_pr = (0..FIR_COEFF_LEN)
            .max_by(|&a, &b| {
                RA_COEFF_LOSSLESS[a]
                    .abs()
                    .total_cmp(&RA_COEFF_LOSSLESS[b].abs())
            })
            .unwrap();
        let max_npr = (0..FIR_COEFF_LEN)
            .max_by(|&a, &b| RA_COEFF_LOSSY[a].abs().total_cmp(&RA_COEFF_LOSSY[b].abs()))
            .unwrap();
        assert!(
            max_pr == 255 || max_pr == 256,
            "lossless peak at {max_pr}, expected the 255/256 centre"
        );
        assert!(
            max_npr == 255 || max_npr == 256,
            "lossy peak at {max_npr}, expected the 255/256 centre"
        );
    }

    #[test]
    fn the_two_tables_are_distinct_sets() {
        // §C.2.5 branches between two different coefficient sets;
        // they must not have collapsed into one during generation.
        assert!(
            (0..FIR_COEFF_LEN).any(|i| RA_COEFF_LOSSLESS[i] != RA_COEFF_LOSSY[i]),
            "perfect and non-perfect tables are identical"
        );
    }

    #[test]
    fn fir_coeff_len_matches_the_c25_fir_bound() {
        // §C.2.5's FIR step iterates `for (j=0; j<512; j+=64)` over
        // 512-tap prCoeff sets; the table length is fixed at 512.
        assert_eq!(FIR_COEFF_LEN, 512);
        assert_eq!(RA_COEFF_LOSSLESS.len(), FIR_COEFF_LEN);
        assert_eq!(RA_COEFF_LOSSY.len(), FIR_COEFF_LEN);
    }
