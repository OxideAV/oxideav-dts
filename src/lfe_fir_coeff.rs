//! §D.8 LFE interpolation FIR coefficient tables for the DTS Core
//! low-frequency-effects (LFE) reconstruction path.
//!
//! Transcribed verbatim from ETSI TS 102 114 V1.3.1 (2011-08)
//! Annex D §D.8 "32-Band Interpolation and LFE Interpolation FIR"
//! (staged at `docs/audio/dts/etsi-ts-102114-dts-coherent-acoustics.pdf`,
//! PDF p.238-246), columns "64 x Interpolation" and "128 x
//! Interpolation" (indices 0..=511; the spec table prints decimal
//! commas, rendered here as decimal points). These are the two
//! 512-tap (`NumFIRCoef = 512`) coefficient sets the §C.2.6
//! `InterpolationFIR(nDecimationSelect)` LFE driver selects, per the
//! resolution in `docs/audio/dts/dts-qmf-driver.md` §3:
//!
//! - [`RA_COEFF_LFE64`] is the spec pseudocode's `raCoeff64`
//!   identifier — the **64x interpolation** set, selected when
//!   `nDecimationSelect == 0` (decimation factor 64).
//! - [`RA_COEFF_LFE128`] is the spec pseudocode's `raCoeff128`
//!   identifier — the **128x interpolation** set, selected when
//!   `nDecimationSelect == 1` (decimation factor 128).
//!
//! [`crate::LfeInterpolationSelection::coefficients`] resolves the
//! typed selector to the matching table.
//!
//! Unlike the two 32-band synthesis-QMF columns of the same §D.8
//! table ([`crate::RA_COEFF_LOSSLESS`] / [`crate::RA_COEFF_LOSSY`],
//! which are *anti-symmetric*, `coeff[i] == -coeff[511 - i]`), the
//! two LFE columns are **symmetric** about the centre
//! (`coeff[i] == coeff[511 - i]` for every `i`) — the expected
//! structure of a symmetric linear-phase interpolation prototype.
//! The test suite verifies that whole-table property plus verbatim
//! anchor rows (`docs/audio/dts/tables/dts-d8-fir.meta.md`
//! "Sample values").
//!
//! # Scope: tables only
//!
//! This module lands the §D.8 LFE coefficient *data* and the typed
//! [`crate::LfeInterpolationSelection`] selector. The §C.2.6
//! `InterpolationFIR()` driver's per-sample polyphase convolution loop
//! body consumes these tables and is implemented in
//! [`crate::LfeInterpolator`] (`src/lfe_synth.rs`), transcribed from
//! `docs/audio/dts/dts-lfe-interpolation-and-audio-walker.md` §1.

