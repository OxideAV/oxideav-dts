//! DTS Coherent Acoustics — Annex D §D.5 audio-data quantization-index
//! Huffman code books (the low-to-mid `ABITS` families), feeding the §5.5
//! Table 5-29 `nQType == 1` ("Huffman code") `AUDIO[m]` extraction
//! path.
//!
//! Source: ETSI TS 102 114 V1.3.1 (2011-08), staged PDF at
//! `docs/audio/dts/etsi-ts-102114-dts-coherent-acoustics.pdf`.
//!
//! # What this module covers
//!
//! The §5.5 `Audio Data` walker dispatches each subband on its
//! `nQType` (see [`crate::audio_quant_type`]). For `nQType == 1` the
//! eight `AUDIO[m]` quantization indices are Huffman-coded, decoded
//! through the §D.5 code book that the `(ABITS, SEL)` pair selects
//! (Table 5-26, staged PDF p.27). This module transcribes the
//! **signed-level** audio-data code books for the lowest `ABITS`
//! families — the ones whose mid-tread quantizer has a small enough
//! level count to print a full code book:
//!
//! | ABITS | levels | clause  | SEL → book                       |
//! |-------|--------|---------|----------------------------------|
//! | 1     | 3      | §D.5.1  | SEL 0 → `A3`                     |
//! | 2     | 5      | §D.5.3  | SEL 0/1/2 → `A5/B5/C5`          |
//! | 3     | 7      | §D.5.4  | SEL 0/1/2 → `A7/B7/C7`          |
//! | 4     | 9      | §D.5.5  | SEL 0/1/2 → `A9/B9/C9`          |
//! | 5     | 13     | §D.5.7  | SEL 0/1/2 → `A13/B13/C13`       |
//! | 6     | 17     | §D.5.8  | SEL 0..6 → `A17/B17/…/G17`      |
//! | 7     | 25     | §D.5.9  | SEL 0..6 → `A25/B25/…/G25`      |
//! | 8     | 33     | §D.5.10 | SEL 0..6 → `A33/B33/…/G33`      |
//! | 9     | 65     | §D.5.11 | SEL 0..6 → `A65/B65/…/G65`      |
//! | 10    | 129    | §D.5.12 | SEL 0..6 → `A129/B129/…/G129`   |
//!
//! (The terminal `SEL` entry of each group is the `V…` block code —
//! `nQType == 3`, handled by [`crate::decode_block_code`] — not a
//! Huffman book, so it is *not* in this module. For the 17-level group
//! that terminal entry is `V17` at SEL 7.)
//!
//! Unlike the §5.4.1 side-information code books (BHUFF/THUFF/SHUFF),
//! whose symbols are *unsigned* indices, the audio-data books decode
//! to **signed** quantization levels: the printed "Quantization level"
//! column runs `0, 1, -1, 2, -2, …`, mirroring the mid-tread
//! quantizer's symmetric output. The decoded level is the signed
//! integer `AUDIO[m]` that §5.5 then scales by `rScale`
//! ([`crate::dequant_subsubframe`]).
//!
//! # Scope and follow-ups
//!
//! For `ABITS 8..=10` the Table 5-26 group is `A… B… C… D… E… F… G…
//! NFE` (eight entries): SEL 0..6 select the seven Huffman books and
//! the terminal SEL 7 the "no further encoding" fixed-width field
//! (`nQType == 2`), *not* a `V…` block code. All three higher families
//! (§D.5.10/§D.5.11/§D.5.12) are now transcribed here, so the §5.5
//! per-subsubframe `Audio Data` walker can dispatch any
//! `nQType == 1` subband for every `ABITS 1..=10`.
//!
//! The Table 5-26 (staged PDF p.27) `(ABITS, SEL)` mapping is the
//! authoritative selector: for ABITS 5 (13 levels) the group is
//! `A13 B13 C13 V13`, so SEL 0/1/2 select the Huffman books and SEL 3
//! the terminal `V13` 4-element block code ([`crate::decode_block_code`],
//! `nQType == 3`).

use crate::bitreader::BitReader;
use crate::{Error, Result};

/// One entry of a §D.5 audio-data Huffman code book:
/// `(quantization_level, code_length, code)`. The codeword is the low
/// `code_length` bits of `code`, read MSB-first from the bit stream
/// (the [`BitReader::read_bits`] convention used throughout this
/// crate). `quantization_level` is the **signed** mid-tread output
/// level the §5.5 `AUDIO[m]` index carries.
type AudioHuffEntry = (i16, u8, u16);

/// Maximum code length across every audio-data Huffman book transcribed
/// here, the bound the bit-at-a-time decoder walks to before declaring a
/// stream-format failure. The longest printed codes are 16 bits, in the
/// 65-level §D.5.11 and 129-level §D.5.12 families (e.g. §D.5.11 `A65`'s
/// ±31/±32 codes 40 543/40 542/40 541/40 540 and §D.5.12 `D129`'s ±64);
/// the 33-level §D.5.10 family tops out at 14 (`D33` ±15/±16), the
/// 25-level §D.5.9 `A25` likewise at 14, and the smaller families far
/// below. The decoder still stops at each book's own
/// [`AudioHuffCodebook::max_code_len`] once a prefix-matched code is
/// found, so this only caps the worst case.
const MAX_AUDIO_HUFF_CODE_LEN: u32 = 16;

